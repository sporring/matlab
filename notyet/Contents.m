% DIKU programs
% Version 1.0 18-Jun-1998
%
% Image file I/O.
%   DIKUREAD Read a DIKU greylevel image as a matrix or vector.
%   DIKUWRITE Write a matrix or vector as a DIKU image.
%
% Diffusion programs
%   SCALE  Gaussian Scale-Space using the Fourier Domain
%   SCALE2 An improved version of SCALE
%   SCALEN An n-dimensional version of SCALE2
%   SCALESPACE calculates smoothed and possibly derived versions of FI
%   LINEAR_DIFFUSION performs recursively the linear diffusion equation
%   AMSS Affine Morphological Scale Space
%   CED Coherence-Enhancing Anisotropic Diffusion
%   DILATION Morphological Gray-value dilation
%   EED Edge-Enhancing Anisotropic Diffusion
%   EROSION Morphological Gray-value erosion
%   ILD Isotropic Linear Diffusion
%   IND Isotropic Nonlinear Diffusion
%   IND_AOS Isotropic Nonlinear Diffusion with Additive Operator Splitting (AOS)
%   MCM Mean Curvature Motion
%
% Information theory programs
%   ENTROPY The information theoretic entropy
%   INFORMATION The information theoretic generalized entropy
%   INF2SPECT Calculate multifractal spectrum as function of exponents and scale
%   INFORMATION_SCALE Calculate the information as function of order and scale
%   SPECTRUM Calculate multifractal spectrum as function of exponents and scale
%   LOCAL_INFORMATION Calculate the local entropy map of an image
%
% Random Images
%   FBM_IMAGE calculates a random fractal Brownian Motion Image
%
% Image extenders
%   POWER2EXPAND Expand image to nearest higher power of 2 using imresize
%   EXTEND Periodic flip-extension of matrices
%
% Clustering algorithms
%   KMEAN  Calculate the matrix minimizing the squared distance using fewer values.
%
% Snake algorithms
%   SNAKE computes one iteration of the energy-minization of active contour models
%   SNAKE_DEMO provides an interactive GUI for showing how the snake m-file works
%
% Various image tools
%   USEORIGO  Setup path for use of the origo matlab suit
%   ZEROCROSSING evaluate the fractional index of the zero crossings of a vector
%
% Various tools
%    BINARY_TO_INT Convert a binary number into an integer
%    HAMMING_DISTANCE The hamming distance between two binary matrices
%    INT_TO_BINARY Convert an integer to binary form.
%    REMOVE_SPACE removes all extra spaces in a string
%    SIGN2  Alternative sign calculation.
